#' Utilities for Calculating Infant and Young Child Feeding Indicators
#'
#' The first 1,000 days of life (from pregnancy to a child’s 2nd birthday) are
#' critical for addressing childhood malnutrition, especially stunting. Infant
#' and young child feeding practices (IYCF) largely overlap with this period as
#' they cover breastfeeding and complementary feeding practices for the first
#' two years of a child’s life. They also have a significant impact on childhood
#' health, nutrition outcomes, and child survival. It is, therefore, critical
#' for countries to measure IYCF practices as part of their efforts to monitor
#' their progress toward Sustainable Development Goal 2. It is also important
#' for development agencies to be able to monitor and evaluate their programs
#' aimed at improving infant and young child feeding practices towards improved
#' overall childhood nutrition.
#'
#' WHO and UNICEF released the first IYCF indicators definition and measurement
#' guidelines in 2008. In 2021, these guidelines were updated along with a
#' revised standard questionnaire to capture the information required to
#' calculate the updated IYCF indicators since the first initial publication.
#' In general, the IYCF indicators can be categorized into three main
#' categories: (1) breastfeeding indicators, (2) complementary indicators, and
#' (3) other indicators, which are focused on bottle-feeding practices and the
#' generation of data visualization plots for the breastfeeding status by age.
#'
